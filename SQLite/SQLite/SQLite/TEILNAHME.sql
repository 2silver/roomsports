CREATE TABLE IF NOT EXISTS `TEILNAHME` (
  `TEIL_ID` INTEGER(20) PRIMARY KEY ASC,
  `TEIL_USER_ID` INTEGER(20) NOT NULL,
  `TEIL_RENN_ID` INTEGER(20) NOT NULL,
  `ANGEMELDET_AM` TEXT(10),
  `PLATZ` INTEGER(11),
  `ZEIT` TEXT(16),
